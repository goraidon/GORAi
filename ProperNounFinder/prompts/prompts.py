class ProperNounAnalyzer:
    """固有名詞分析用プロンプトクラス"""
    
    @staticmethod
    def get_analysis_prompt(text):
        """固有名詞分析用のプロンプトを生成"""
        prompt = f"""
以下のテキストを分析して、固有名詞が含まれているかどうかを判定してください。

テキスト: "{text}"

以下の形式でJSONで回答してください：
{{
    "contains_proper_noun": true/false,
    "reason": "判定理由を日本語で記載",
    "proper_nouns": ["固有名詞1", "固有名詞2"],
    "replaced_text": "固有名詞を一般的な表現に置き換えた文章"
}}

固有名詞の定義：
- 人名（実在・架空問わず）
- 地名（国名、都市名、地域名など）
- 会社名・組織名・ブランド名
- 商品名・サービス名
- 映画・書籍・楽曲などの作品名
- その他特定の固有の名称

置き換え例：
- 人名 → "ある人"、"その人"、"彼/彼女"
- 会社名 → "ある会社"、"その企業"
- 地名 → "ある地域"、"その場所"
- 商品名 → "ある商品"、"その製品"
"""
        return prompt 